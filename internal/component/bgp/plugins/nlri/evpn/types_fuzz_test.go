package evpn

import (
	"testing"
)

// FuzzParseEVPN tests EVPN NLRI binary parsing robustness.
//
// VALIDATES: ParseEVPN handles arbitrary bytes without crashing.
// PREVENTS: Panic on truncated data, unknown route types, malformed MAC/IP/ESI/RD.
// SECURITY: EVPN bytes come from untrusted BGP UPDATE NLRI.
func FuzzParseEVPN(f *testing.F) {
	// Type 1: Ethernet Auto-Discovery
	// route-type=1, length=25, RD(8) + ESI(10) + EthTag(4) + Label(3)
	f.Add([]byte{
		0x01, 0x19, // type 1, length 25
		0x00, 0x00, 0x00, 0x01, 0x00, 0x00, 0x00, 0x01, // RD type 0
		0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, // ESI all zeros
		0x00, 0x00, 0x00, 0x64, // ethernet tag 100
		0x00, 0x06, 0x41, // label 100 BOS
	})
	// Type 2: MAC/IP Advertisement (MAC only)
	// route-type=2, length=33, RD(8) + ESI(10) + EthTag(4) + MACLen(1) + MAC(6) + IPLen(0) + Label(3)
	f.Add([]byte{
		0x02, 0x21, // type 2, length 33
		0x00, 0x00, 0x00, 0x01, 0x00, 0x00, 0x00, 0x01, // RD
		0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, // ESI
		0x00, 0x00, 0x00, 0x64, // ethernet tag
		0x30,                               // MAC len = 48
		0x11, 0x22, 0x33, 0x44, 0x55, 0x66, // MAC
		0x00,             // IP len = 0
		0x00, 0x06, 0x41, // label
	})
	// Type 2: MAC/IP Advertisement (MAC + IPv4)
	f.Add([]byte{
		0x02, 0x25, // type 2, length 37
		0x00, 0x00, 0x00, 0x01, 0x00, 0x00, 0x00, 0x01,
		0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
		0x00, 0x00, 0x00, 0x64,
		0x30,
		0x11, 0x22, 0x33, 0x44, 0x55, 0x66,
		0x20,                   // IP len = 32 (IPv4)
		0x0A, 0x00, 0x00, 0x01, // 10.0.0.1
		0x00, 0x06, 0x41,
	})
	// Type 3: Inclusive Multicast Ethernet Tag
	// route-type=3, length=17, RD(8) + EthTag(4) + IPLen(1) + IP(4)
	f.Add([]byte{
		0x03, 0x11,
		0x00, 0x00, 0x00, 0x01, 0x00, 0x00, 0x00, 0x01,
		0x00, 0x00, 0x00, 0x64,
		0x20,
		0x0A, 0x00, 0x00, 0x01,
	})
	// Type 4: Ethernet Segment
	f.Add([]byte{
		0x04, 0x17, // type 4, length 23
		0x00, 0x00, 0x00, 0x01, 0x00, 0x00, 0x00, 0x01,
		0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
		0x20,
		0x0A, 0x00, 0x00, 0x01,
	})
	// Type 5: IP Prefix Route
	f.Add([]byte{
		0x05, 0x22, // type 5, length 34
		0x00, 0x00, 0x00, 0x01, 0x00, 0x00, 0x00, 0x01,
		0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
		0x00, 0x00, 0x00, 0x64,
		0x18,                   // prefix len = 24
		0x0A, 0x00, 0x00, 0x00, // 10.0.0.0
		0x20,                   // GW IP len = 32
		0x0A, 0x00, 0x00, 0x01, // gateway
		0x00, 0x06, 0x41, // label
	})
	f.Add([]byte{})                                   // Empty
	f.Add([]byte{0x01})                               // Type only
	f.Add([]byte{0x01, 0x00})                         // Type + zero length
	f.Add([]byte{0xFF, 0xFF, 0xFF})                   // Unknown type, max values
	f.Add([]byte{0x06, 0x04, 0x01, 0x02, 0x03, 0x04}) // Unknown type 6

	f.Fuzz(func(t *testing.T, data []byte) {
		evpn, _, err := ParseEVPN(data, false)
		if err != nil {
			return
		}
		// Exercise methods — MUST NOT panic
		_ = evpn.String()
		_ = evpn.Bytes()
		_ = evpn.Len()
		buf := make([]byte, evpn.Len()+10)
		_ = evpn.WriteTo(buf, 0)
		_ = evpn.RouteType()
		_ = evpn.Family()
	})
}
